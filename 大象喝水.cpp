/*
描述
一只大象口渴了，要喝20升水才能解渴，但现在只有一个深h厘米，
底面半径为r厘米的小圆桶(h和r都是整数)。问大象至少要喝多少桶水才会解渴。

输入
输入有一行：包行两个整数，以一个空格分开，分别表示小圆桶的深h和底面半径r，
单位都是厘米。

输出
输出一行，包含一个整数，表示大象至少要喝水的桶数。

样例输入
第一组
23 11
第二组
1 1

样例输出
第一组
3
第二组
6367

提示
如果一个圆桶的深为h厘米，底面半径为r厘米，
那么它最多能装Pi * r * r * h立方厘米的水。(设Pi=3.14159)
1升 = 1000毫升
1毫升 = 1 立方厘米
*/
#include <iostream>
using namespace::std;

int main(void)
{
    float PI = 3.14159;
    int h,r;
    //一次输入水桶高h,底面半径r
    cin >> h >> r;
    float volume = (PI*r*r*h)/1000;
    //开始判断
    if(volume <= 20.0)
        cout << (int)(20/volume) + 1<< endl;
    else
        cout << 1 << endl;
    system("pause");
    return 0;
}
